public class ConfigException extends PortletException {

	private static final long serialVersionUID = 2906455225307820370L;

	public ConfigException() {
    }

    public ConfigException(String message) {
        super(message);
    }

    public ConfigException(String message, Throwable cause) {
        super(message, cause);
    }

    public ConfigException(Throwable cause) {
        super(cause);
    }
}
